num3=float(input("Ingres numero 3: "))

#media aritmetica

aritmetica= (num1+num2+num3)/3

#media geometrica

geometrica = ( num1*num2*num3)**(1/3)

print(" La media aritmetica es: %.2f" %aritmetica, " geometrica: %.2f" %geometrica)
